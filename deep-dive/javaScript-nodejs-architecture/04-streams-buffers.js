// Custom Readable Stream
class CounterStream extends Readable {
  constructor(max) {
    super();
    this.max = max;
    this.current = 1;
  }

  _read() {
    if (this.current <= this.max) {
      // Push data as string (or Buffer)
      const chunk = `Number: ${this.current}\n`;
      this.push(chunk);
      this.current++;
    } else {
      // Signal end of stream
      this.push(null);
    }
  }
}

console.log("=== Custom Readable Stream (Counter) ===");
const counter = new CounterStream(3);

// Flowing mode - data event
counter.on('data', (chunk) => {
  console.log("  Received:", chunk.toString().trim());
});

counter.on('end', () => {
  console.log("  Stream ended\n");
});


// Object Mode Readable Stream
class ObjectStream extends Readable {
  constructor(items) {
    super({ objectMode: true }); // Enable object mode
    this.items = items;
    this.index = 0;
  }

  _read() {
    if (this.index < this.items.length) {
      this.push(this.items[this.index++]);
    } else {
      this.push(null);
    }
  }
}

console.log("=== Object Mode Stream ===");
