// When to use sequential
async function sequentialDependency() {
    // Each step depends on the previous
    const user = await delay(50, { id: 1, name: "John" });
    const posts = await delay(50, [{ userId: user.id, title: "Post 1" }]);
    const comments = await delay(50, [{ postId: posts[0].id, text: "Nice!" }]);

    return { user, posts, comments };
}


