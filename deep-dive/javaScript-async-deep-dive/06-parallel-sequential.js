// WRONG - forEach doesn't wait
async function forEachWrong() {
    const items = [1, 2, 3];

    console.log("forEach START");
    items.forEach(async (item) => {
        await delay(50);
        console.log("forEach item:", item);
    });
    console.log("forEach END - BUT ITEMS NOT DONE!");
}

// RIGHT - for...of waits (sequential)
async function forOfSequential() {
    const items = [1, 2, 3];

    console.log("for...of START");
    for (const item of items) {
        await delay(50);
        console.log("for...of item:", item);
    }
    console.log("for...of END - Items are done!");
}

// RIGHT - map + Promise.all (parallel)
async function mapParallel() {
    const items = [1, 2, 3];

    console.log("map + Promise.all START");
    const results = await Promise.all(
        items.map(async (item) => {
            await delay(50);
            console.log("map item:", item);
            return item * 2;
        })
    );
    console.log("map + Promise.all END:", results);
}

setTimeout(() => forEachWrong(), 3000);
setTimeout(() => forOfSequential(), 3200);
setTimeout(() => mapParallel(), 3600);


