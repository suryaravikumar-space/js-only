async function parallel() {
    console.time("Parallel");

    const [a, b, c] = await Promise.all([
        delay(100, "A"),
        delay(100, "B"),
        delay(100, "C")
    ]);

    console.log("Got all:", a, b, c);
    console.timeEnd("Parallel");  // ~100ms
    return [a, b, c];
}

setTimeout(() => {
    parallel().then(r => console.log("Parallel result:", r));
}, 500);


// Alternative: Start promises first, await later
async function parallelManual() {
    console.time("Parallel Manual");

    // Start all promises immediately
    const promiseA = delay(100, "A");
    const promiseB = delay(100, "B");
    const promiseC = delay(100, "C");

    // Then await them
    const a = await promiseA;
    const b = await promiseB;
    const c = await promiseC;

    console.timeEnd("Parallel Manual");  // ~100ms
    return [a, b, c];
}


