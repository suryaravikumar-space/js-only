function* fibonacci() {
    let [prev, curr] = [0, 1];
    while (true) {
        yield curr;
        [prev, curr] = [curr, prev + curr];
    }
}

// Take first n values
function take(n, iterable) {
    const result = [];
    for (const value of iterable) {
        if (result.length >= n) break;
        result.push(value);
    }
    return result;
}

