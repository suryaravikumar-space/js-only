async function validateAll(validators, data) {
    const errors = [];

    for (const validator of validators) {
        try {
            await validator(data);
        } catch (error) {
            errors.push(error.message);
        }
    }

    if (errors.length > 0) {
        throw new Error(`Validation failed:\n  - ${errors.join('\n  - ')}`);
    }

    return true;
}

