function delay(ms, value) {
    return new Promise(resolve => setTimeout(() => resolve(value), ms));
}

// PROMISE VERSION
function getDataPromise() {
    return delay(100, { id: 1, name: "John" })
        .then(user => {
            console.log("Promise: Got user", user.name);
            return delay(100, [{ id: 1, title: "Post" }]);
        })
        .then(posts => {
            console.log("Promise: Got posts", posts.length);
            return posts;
        });
}

// ASYNC/AWAIT VERSION
async function getDataAsync() {
    const user = await delay(100, { id: 1, name: "John" });
    console.log("Async: Got user", user.name);

    const posts = await delay(100, [{ id: 1, title: "Post" }]);
    console.log("Async: Got posts", posts.length);

    return posts;
}

// Both produce the same result!
getDataPromise();
