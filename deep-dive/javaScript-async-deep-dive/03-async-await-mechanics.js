// SEQUENTIAL - Each await waits for the previous (SLOW)
async function sequential() {
    console.time("Sequential");

    const a = await delay(100, "A");
    const b = await delay(100, "B");
    const c = await delay(100, "C");

    console.timeEnd("Sequential");  // ~300ms
    return [a, b, c];
}

// PARALLEL - Start all at once (FAST)
async function parallel() {
    console.time("Parallel");

    // Start all promises immediately
    const pA = delay(100, "A");
    const pB = delay(100, "B");
    const pC = delay(100, "C");

    // Wait for all
    const a = await pA;
    const b = await pB;
    const c = await pC;

    console.timeEnd("Parallel");  // ~100ms
    return [a, b, c];
}

// PARALLEL with Promise.all (CLEANEST)
async function parallelAll() {
    console.time("Promise.all");

    const [a, b, c] = await Promise.all([
        delay(100, "A"),
        delay(100, "B"),
        delay(100, "C")
    ]);

    console.timeEnd("Promise.all");  // ~100ms
    return [a, b, c];
}

// Run demos
sequential();
setTimeout(() => parallel(), 500);
setTimeout(() => parallelAll(), 1000);


