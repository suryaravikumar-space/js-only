// WRONG - forEach doesn't wait!
async function wrongLoop() {
    const items = [1, 2, 3];

    console.log("Wrong loop start");
    items.forEach(async (item) => {
        await delay(50, item);
        console.log("forEach item:", item);
    });
    console.log("Wrong loop end (runs BEFORE items!)");
}

// CORRECT - for...of waits
async function correctLoop() {
    const items = [1, 2, 3];

    console.log("Correct loop start");
    for (const item of items) {
        await delay(50, item);
        console.log("for...of item:", item);
    }
    console.log("Correct loop end (runs AFTER items!)");
}

// PARALLEL loop with Promise.all + map
async function parallelLoop() {
    const items = [1, 2, 3];

    const results = await Promise.all(
        items.map(async (item) => {
            await delay(50, item);
            return item * 2;
        })
    );

    console.log("Parallel loop results:", results);
}

